/******************************************************************************/
/* init mq connection      */
/******************************************************************************/
int initMq( )
{
  logFuncCall( ) ;

  MQLONG sysRc = MQRC_NONE ;

  MQHCONN hConn ;

  char defQmgr[1] ; defQmgr[0] = 0 ;
  char *qmgr    = getStrAttr( "qmgr" );
  
  if( qmgr == NULL )
  {
    qmgr = defQmgr ;
  }

  sysRc = mqConn( qmgr, &hConn ) ;  
  switch( sysRc )
  {
    case MQRC_NONE : break ;
    default        : goto _door ;
  }

  _door :

  logFuncExit( ) ;

  return sysRc ;
}
