/******************************************************************************/
/*  console worker                                              */
/******************************************************************************/
int consoleWorker()
{
  logFuncCall( ) ;

  int sysRc = 0 ;

  sysRc = initMq();
  if( sysRc != 0 )
  {
    goto _door ;
  }

  // -------------------------------------------------------
  // browse messages in input queue
  // -------------------------------------------------------
  browseEvents();

  handleDoneEvents();

  // -------------------------------------------------------
  // list events
  // -------------------------------------------------------
  printAllEventList();

  _door :

  logFuncExit( ) ;
  return sysRc ;
}


/******************************************************************************/
/*  html worker                        */
/******************************************************************************/
int htmlWorker()
{
  logFuncCall( ) ;
  int sysRc = 0 ;

  tIniNode *searchIni ;    // data structure for getting searching in ini files
  char     *wwwDir    ;    // directory for raw html data

  searchIni = getIniNode( "system", "html" );  // system.html node from ini
  wwwDir = getIniStrValue( searchIni,"dir" );  // get file & level from node
  if( !wwwDir )
  {
    sysRc = 1;
    goto _door;
  }

  sysRc = initMq();
  if( sysRc != 0 )
  { 
    goto _door ;
  }

  // -------------------------------------------------------
  // browse messages in input queue
  // -------------------------------------------------------
  browseEvents();

  // -------------------------------------------------------
  // list events on html
  // -------------------------------------------------------
  sysRc = printAllEventTable( wwwDir );
  if( sysRc != 0 )
  {
    goto _door;
  }

  _door :

  logFuncExit( ) ;
  return sysRc ;
}